  Future<bool> _performEditLitten() async {
    final l10n = AppLocalizations.of(context);
    final newTitle = _titleController.text.trim();

    // ÏûÖÎ†• Ïú†Ìö®ÏÑ± Í≤ÄÏÇ¨
    if (newTitle.isEmpty) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text(l10n?.pleaseEnterTitle ?? 'Ï†úÎ™©ÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.')),
        );
      }
      return false;
    }

    // Ïä§ÏºÄÏ§Ñ Ïú†Ìö®ÏÑ± Í≤ÄÏÇ¨
    if (_selectedSchedule != null) {
      final startTime = _selectedSchedule!.startTime;
      final endTime = _selectedSchedule!.endTime;
      if (startTime.hour == endTime.hour && startTime.minute >= endTime.minute) {
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(content: Text('ÏãúÏûë ÏãúÍ∞ÑÏù¥ Ï¢ÖÎ£å ÏãúÍ∞ÑÎ≥¥Îã§ Îä¶ÏùÑ Ïàò ÏóÜÏäµÎãàÎã§.')),
          );
        }
        return false;
      }
    }

    final appState = Provider.of<AppStateProvider>(context, listen: false);
    final scaffoldMessenger = ScaffoldMessenger.of(context);

    try {
      debugPrint('üîÑ Î¶¨Ìäº ÏàòÏ†ï ÏãúÏûë: ${widget.litten.id} - $newTitle');

      // ÏàòÏ†ïÎêú Î¶¨Ìäº ÏÉùÏÑ±
      final updatedLitten = Litten(
        id: widget.litten.id,
        title: newTitle,
        description: widget.litten.description,
        createdAt: widget.litten.createdAt,
        updatedAt: DateTime.now(),
        audioFileIds: widget.litten.audioFileIds,
        textFileIds: widget.litten.textFileIds,
        handwritingFileIds: widget.litten.handwritingFileIds,
        schedule: _selectedSchedule,
      );

      // Î¶¨Ìäº ÏóÖÎç∞Ïù¥Ìä∏
      await appState.updateLitten(updatedLitten);

      if (mounted) {
        final scheduleText = _selectedSchedule != null
            ? ' (${DateFormat('MÏõî dÏùº').format(_selectedSchedule!.date)} ${_selectedSchedule!.startTime.format(context)})'
            : '';
        scaffoldMessenger.showSnackBar(
          SnackBar(content: Text('${updatedLitten.title} Î¶¨ÌäºÏù¥ ÏàòÏ†ïÎêòÏóàÏäµÎãàÎã§.$scheduleText')),
        );
        debugPrint('‚úÖ Î¶¨Ìäº ÏàòÏ†ï ÏôÑÎ£å: ${updatedLitten.id}');
      }
      return true;
    } catch (e) {
      debugPrint('‚ùå Î¶¨Ìäº ÏàòÏ†ï ÏóêÎü¨: $e');
      if (mounted) {
        scaffoldMessenger.showSnackBar(
          SnackBar(content: Text('${l10n?.error ?? 'Ïò§Î•ò'}: $e')),
        );
      }
      return false;
    }
  }

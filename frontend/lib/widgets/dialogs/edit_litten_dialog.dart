  Widget _buildScheduleTabView() {
    return DefaultTabController(
      length: 2,
      initialIndex: _currentTabIndex,
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // ÌÉ≠Î∞î
          TabBar(
            labelColor: _selectedSchedule != null && widget.litten.schedule != null

              });
              widget.onScheduleIndexChanged(index);
            },
            tabs: [
              Tab(
                child: Row(
                  mainAxisAlignment: MainAxisAlignment.center,
                  children: [
                    Icon(
                      _selectedSchedule != null && widget.litten.schedule != null
